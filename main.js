const REVIEWS_KEY = 'golden_team_reviews';
let selectedRating = 0;

function getSavedReviews() {
  try { return JSON.parse(localStorage.getItem(REVIEWS_KEY)) || []; }
  catch { return []; }
}

function saveReview(r) {
  const reviews = getSavedReviews();
  reviews.unshift(r);
  localStorage.setItem(REVIEWS_KEY, JSON.stringify(reviews));
}

function buildTestimonialCard(t, stars) {
  const s = typeof stars === 'number' ? stars : (t.stars || 5);
  return `
    <div class="testimonial-card">
      <div class="testimonial-stars">${starsSvg(s)}</div>
      <p class="testimonial-text">${sanitizeHTML(t.text)}</p>
      <div class="testimonial-author">
        <div class="testimonial-avatar">${t.avatar || t.name.charAt(0)}</div>
        <div>
          <div class="testimonial-name">${sanitizeHTML(t.name)}</div>
        </div>
      </div>
    </div>
  `;
}

function starsSvg(n) {
  return Array.from({length:5}, (_,i) => `<svg width="12" height="12" viewBox="0 0 24 24" fill="${i<n?'#f59e0b':'#d1d5db'}"><path d="m12 2 3.1 6.3 6.9 1-5 4.9 1.2 6.8L12 17.8l-6.2 3.2L7 14.2 2 9.3l6.9-1z"/></svg>`).join('');
}

function loadTestimonials() {
  const grid = document.getElementById('testimonialsTrack1');
  const count = document.getElementById('reviewCount');
  if (!grid) return;
  const lang = getLang();
  const base = lang === 'en' ? TESTIMONIALS_EN : TESTIMONIALS_AR;
  const saved = getSavedReviews();
  const all = [...saved, ...base];
  if (count) count.textContent = all.length;
  grid.innerHTML = all.map(t => buildTestimonialCard(t)).join('') + all.map(t => buildTestimonialCard(t)).join('') + all.map(t => buildTestimonialCard(t)).join('');
}

function setRating(v) {
  selectedRating = v;
  document.querySelectorAll('.star-input-star').forEach(el => {
    const val = parseInt(el.dataset.value);
    el.textContent = val <= v ? '★' : '☆';
    el.classList.toggle('active', val <= v);
  });
}

function submitReview(e) {
  e.preventDefault();
  const name = document.getElementById('reviewName');
  const text = document.getElementById('reviewText');
  if (!name.value.trim() || !text.value.trim() || selectedRating === 0) {
    showToast('الرجاء تعبئة جميع الحقول وتحديد التقييم');
    return false;
  }
  const review = {
    name: sanitizeHTML(name.value.trim()),
    text: sanitizeHTML(text.value.trim()),
    stars: selectedRating,
    avatar: name.value.trim().charAt(0),
    date: new Date().toISOString(),
    role: ''
  };
  saveReview(review);
  showToast('تم إضافة تقييمك بنجاح! شكراً لك.');
  name.value = '';
  text.value = '';
  selectedRating = 0;
  document.querySelectorAll('.star-input-star').forEach(el => { el.textContent = '☆'; el.classList.remove('active'); });
  document.getElementById('reviewFormContainer').classList.remove('show');
  loadTestimonials();
  return false;
}

